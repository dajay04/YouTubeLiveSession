package youtube_lec_wise.lecutre4;

public class ForTest
{
    /*
       5 * 1 = 5(5*1)
       5 * 2 = 10(5*2)
       5 * 3 = 15(5*3)
    */
    public static void main(String[] args)
    {
        int num = 15;
        for(int i=1; i<=10;i++ )
        {
            System.out.println(num+ " * " + i + " = "+ (num*i)); // 5 * 3 = 15
        }
    }
}
